#radius as user input
radius=float(input("enter the radius of the circle: "))

#calculating area of circle
_area_of_circle = 3.14*radius*radius
print("area of circle is: {0}".format(_area_of_circle))

#calculating circumference of circle
_circumference_of_circle = 2*3.14*radius
print("circumference of circle is: {0}".format(_circumference_of_circle))